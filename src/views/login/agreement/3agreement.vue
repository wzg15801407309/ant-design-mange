        id="4-개인정보의-제공-및-위탁"
        class="anchor"
        href="#4-%EA%B0%9C%EC%9D%B8%EC%A0%95%EB%B3%B4%EC%9D%98-%EC%A0%9C%EA%B3%B5-%EB%B0%8F-%EC%9C%84%ED%83%81"
      ></a
      >4. 개인정보의 제공 및 위탁
    </h2>
    <p>
      <strong
        >조이그릿은 원칙적으로 이용자 동의 없이 개인정보를 외부에 제공하지
        않습니다.</strong
      >
    </p>
    <p>
      조이그릿은 이용자의 사전 동의 없이 개인정보를 외부에 제공하지 않습니다.
      단, 이용자가 외부 제휴사의 서비스를 이용하기 위하여 개인정보 제공에 직접
      동의를 한 경우, 그리고 관련 법령에 의거해 조이그릿에 개인정보 제출 의무가
      발생한 경우, 이용자의 생명이나 안전에 급박한 위험이 확인되어 이를 해소하기
      위한 경우에 한하여 개인정보를 제공하고 있습니다.
    </p>
    <p>
      <strong
        >조이그릿은 편리하고 더 나은 서비스를 제공하기 위해 업무 중 일부를
        외부에 위탁하고 있습니다.</strong
      >
    </p>
    <p>
      조이그릿은 서비스 제공을 위하여 필요한 업무 중 일부를 외부 업체에 위탁하고
      있으며, 위탁받은 업체가 정보통신망법에 따라 개인정보를 안전하게 처리하도록
      필요한 사항을 규정하고 관리/감독을 하고 있습니다. 조이그릿이 수탁업체에
      위탁하는 업무와 관련된 서비스를 이용하지 않는 경우, 이용자의 개인정보가
      수탁업체에 제공되지 않습니다.
    </p>
    <blockquote>
      <p><strong>개인정보 제공 및 위탁에 대한 추가 설명</strong></p>
      <p><strong>(1) 개인정보 제공과 위탁의 차이</strong></p>
      <p>
        개인정보의 제3자 제공은 제공받는 자의 업무 처리와 이익을 위하여
        개인정보가 제공되는 것을 의미합니다. 개인정보가 제공된 이후에는 제공받는
        자의 책임 하에 개인정보가 처리됩니다. 이런 이유로 개인정보의 제3자
        제공을 위해서는 이용자에게 사전에 ‘개인정보를 제공받는 자, 개인정보를
        제공받는 자의 개인정보 이용 목적, 제공하는 개인정보의 항목, 개인정보를
        제공받는 자의 개인정보 보유 및 이용 기간’에 대해 고지하고 동의를
        받습니다. 개인정보의 처리위탁은 제공하는 자의 업무 처리를 위하여
        개인정보를 외부에 위탁하는 것을 의미합니다. 개인정보가 제공된 이후에도
        개인정보를 제공한 자, 즉 위탁자에게 수탁 업체에 대한 관리/감독 책임이
        발생합니다.
      </p>
      <p>
        <strong
          >(2) 이용자 동의가 필요한 위탁업무와 공개/통지로 동의를 갈음할 수 있는
          위탁업무</strong
        >
      </p>
      <p>
        서비스 제공에 관한 계약을 이행하고 이용자 편의 증진 등을 위해 필요한
        개인정보 처리위탁은 메일 등을 통해 이용자에게 통지하거나
        개인정보처리방침에 공개하는 것으로 동의를 갈음할 수 있습니다. 그 외에
        개인정보 처리위탁에 대해서는 ‘수탁 업체, 위탁업무 내용’을 이용자에게
        고지하고 사전에 동의를 받아야만 합니다. 조이그릿은 인프라 운영, 서비스
        개발 및 테스트, 서비스 운영, 고객상담, 결제처리, 본인인증 등을 위한
        개인정보 처리위탁을 개인정보처리방침에 공개하는 것으로 동의를
        갈음합니다. 단, 서비스 프로모션 등을 위해 외부에 개인정보를 위탁하는
        경우에는 사전에 이용자 동의를 받습니다.
      </p>
    </blockquote>
